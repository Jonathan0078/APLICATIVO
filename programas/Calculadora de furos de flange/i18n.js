  function t(key) {
    return dict[current]?.[key] ?? dict['pt']?.[key] ?? key;
  }

  function translatePage(root) {
    const scope = root || document;
    
    // Traduz textos normais e títulos
    scope.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      if (el.tagName === 'TITLE') {
        el.textContent = t(key);
      } else if (el.tagName !== 'INPUT' && el.tagName !== 'TEXTAREA') {
        el.textContent = t(key);
      }
    });

    // Traduz placeholders especificamente
    scope.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      el.placeholder = t(el.getAttribute('data-i18n-placeholder'));
    });

    // Traduz atributos title (tooltips)
    scope.querySelectorAll('[data-i18n-title]').forEach(el => {
      el.title = t(el.getAttribute('data-i18n-title'));
    });
  }


  function switchLang(code) {
    if (!['pt', 'es', 'en'].includes(code)) return;
    localStorage.setItem('site-lang', code);
    location.reload();
  }

  function addLangSelector(container) {
    const sel = document.createElement('select');
    sel.className = 'lang-select';
    ['pt', 'es', 'en'].forEach(code => {
      const opt = document.createElement('option');
      opt.value = code;
      opt.textContent = dict[current]?.['lang.' + code] || dict['pt']['lang.' + code];
      if (code === current) opt.selected = true;
      sel.appendChild(opt);
    });
    sel.addEventListener('change', () => switchLang(sel.value));
    container.appendChild(sel);
  }

  function updateToolCounts() {
    document.querySelectorAll('.category-section').forEach(section => {
      const countEl = section.querySelector('.tool-count');
      if (!countEl) return;
      if (section.id === 'favorites-section') {
        const items = section.querySelectorAll('.tool-item');
        const n = items.length;
        countEl.textContent = n === 0 ? t('favorites.empty') : `${n} ${n === 1 ? t('common.ferramenta') : t('common.ferramentas')}`;
        return;
      }
      const items = section.querySelectorAll('.tool-item:not(.hidden)');
      const n = items.length;
      countEl.textContent = `${n} ${n === 1 ? t('common.ferramenta') : t('common.ferramentas')}`;
    });
  }

  const cssInjected = false;
  function injectCSS() {
    if (cssInjected) return;
    const style = document.createElement('style');
    style.textContent = `
.lang-selector-container { margin-top:6px; }
.lang-select,.lang-select-floating{background:rgba(255,255,255,0.12);border:1px solid rgba(255,255,255,0.15);border-radius:6px;color:#fff;font-family:inherit;font-size:.75rem;padding:.25rem .5rem;cursor:pointer;outline:none;-webkit-appearance:none;appearance:none}
.lang-select option,.lang-select-floating option{background:#1e293b;color:#fff}
.lang-select-floating{position:fixed;top:8px;right:8px;z-index:9999;background:rgba(15,23,42,0.85);backdrop-filter:blur(8px);border-color:rgba(255,255,255,0.2);padding:.3rem .6rem;font-size:.8rem}`;
    document.head.appendChild(style);
  }

  document.addEventListener('DOMContentLoaded', () => {
    injectCSS();
    translatePage();
    document.documentElement.lang = langMap[current] || 'pt-BR';
    updateToolCounts();
    const langContainer = document.getElementById('lang-selector-container');
    if (langContainer) {
      addLangSelector(langContainer);
    }
  });

  return { t, translatePage, switchLang, addLangSelector, updateToolCounts, current, lang: current };
